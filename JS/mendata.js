function displayProducts(data) {
  // console.log(data);
  document.querySelector("#main2").innerText = "";
  data.forEach(function (ele) {
    let mainDiv = document.createElement("div");
    // mainDiv.innerText = "";
    mainDiv.setAttribute("class", "cont");
    let imgDiv = document.createElement("div");
    imgDiv.setAttribute("class", "contimg");
    let productDiv = document.createElement("div");
    productDiv.setAttribute("class", "contdata");

    //   // create a new img tag, set src
    let productImg = document.createElement("img");
    productImg.setAttribute("src", ele.img_url);
    productImg.setAttribute("class", "img_css");
    //   // create a new p/div tag for name
    let productBrand = document.createElement("p");
    productBrand.innerText = ele.brand;
    productBrand.setAttribute("class", "para1");

    let productName = document.createElement("p");
    productName.innerText = ele.name;
    productName.setAttribute("class", "para2");

    let productPrice = document.createElement("p");
    productPrice.innerText = ele.price;
    productPrice.setAttribute("class", "para3");

    let addBtn = document.createElement("button");
    addBtn.innerText = "Add to Cart";
    addBtn.setAttribute("class", "addtocart");
    addBtn.addEventListener("click", addToCart);
    function addToCart(e) {
      let cartItems = JSON.parse(localStorage.getItem("cart")) || [];
      cartItems.push(ele);
      localStorage.setItem("cart", JSON.stringify(cartItems));
    }
    imgDiv.append(productImg);
    productDiv.append(productBrand, productName, productPrice, addBtn);
    mainDiv.append(imgDiv, productDiv);
    document.querySelector("#main2").append(mainDiv);
  });
}

displayProducts(womensData);

// gneder filter
let genderFilter = document.querySelector("#genderoption");
genderFilter.value = "male";
genderFilter.addEventListener("change", function (e) {
  e.preventDefault();
  if (genderFilter.value === "female") {
    window.location = "womenlanding.html";
  }
});

//condition filter
let conditionFilter = document.querySelector("#conditionoption");
conditionFilter.addEventListener("change", function (e) {
  let conditionValue = conditionFilter.value;
  let filteredData = null;
  if (conditionValue === "all") {
    filteredData = womensData;
  } else {
    filteredData = womensData.filter(function (val) {
      // console.log(val.condition);
      return conditionValue === val.condition;
    });
  }
  displayProducts(filteredData);
});

// category filter
let categoryFilter = document.querySelector("#categoryoption");
categoryFilter.addEventListener("change", function (e) {
  let categoryValue = categoryFilter.value;
  let categoryData = null;
  if (categoryValue === "all") {
    categoryData = womensData;
  } else {
    categoryData = womensData.filter(function (val) {
      return val.category === categoryValue;
    });
  }
  displayProducts(categoryData);
});

let relatedSearchFilter = document.querySelectorAll(".related_search > div");
relatedSearchFilter.forEach(function (val) {
  val.addEventListener("click", function (e) {
    let related_search_item = val.className;
    if (related_search_item === "rswc") {
      window.location = "womenlanding.html";
    } else if (related_search_item === "rsmc") {
      let filteredData = womensData.filter(function (val) {
        return val.category === "c";
      });
      displayProducts(filteredData);
    } else if (related_search_item === "rsma") {
      let filteredData = womensData.filter(function (val) {
        return val.category === "a";
      });
      displayProducts(filteredData);
    } else {
      let filteredData = womensData.filter(function (val) {
        return val.brand === related_search_item;
      });
      displayProducts(filteredData);
    }
  });
});
